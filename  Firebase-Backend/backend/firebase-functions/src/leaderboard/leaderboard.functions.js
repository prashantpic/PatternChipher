const functions = require("firebase-functions");
const admin = require("../core/firebaseAdminSetup").admin; // Assuming firebaseAdminSetup.js exports admin
const logger = require("../core/logger");
const errorHandler = require("../core/errorHandler");
const scoreValidator = require("./score.validator"); // Assumed to exist
const rateLimiter = require("./rate.limiter"); // Assumed to exist
const firestoreService = require("../services/firestore.service");
const config = require("../config/index");

/**
 * @file Cloud Functions for leaderboard interactions. Includes score submission and data querying.
 * @namespace PatternCipher.Backend.Leaderboard
 */

/**
 * Submits a score to the leaderboard after validation and rate limiting.
 * REQ-9-002: Leaderboard Score Submission API
 * REQ-9-009: Score Validation (invokes validator)
 * REQ-CPS-012: Leaderboard Integrity (invokes validator and rate limiter)
 * @async
 * @function submitScore
 * @param {object} scoreData - The score data to submit. Expected to conform to LeaderboardEntry schema.
 * @param {functions.https.CallableContext} context - The context of the function call, containing auth information.
 * @returns {Promise<object>} A promise that resolves with an object indicating success or error.
 * @throws {functions.https.HttpsError} Throws an HttpsError for authentication failures, validation errors, rate limiting, or internal errors.
 */
exports.submitScore = functions.https.onCall(async (scoreData, context) => {
    logger.info("submitScore function called", { scoreData, userId: context.auth ? context.auth.uid : "anonymous" });

    if (!context.auth || !context.auth.uid) {
        logger.warn("submitScore: Authentication required.", { ip: context.rawRequest.ip });
        throw errorHandler.toHttpsError(new functions.https.HttpsError("unauthenticated", "Authentication required to submit a score."));
    }
    const userId = context.auth.uid;

    try {
        // REQ-9-009, REQ-CPS-012: Validate score data
        const { isValid, errors, validatedScore } = await scoreValidator.validateScoreData(scoreData, userId);
        if (!isValid) {
            logger.warn("submitScore: Invalid score data.", { userId, errors, scoreData });
            throw errorHandler.toHttpsError(new functions.https.HttpsError("invalid-argument", `Invalid score data: ${errors.join(", ")}`));
        }

        // REQ-9-009, REQ-CPS-012: Rate limiting
        const actionType = "submit_score";
        if (await rateLimiter.isRateLimited(userId, actionType)) {
            logger.warn("submitScore: User is rate limited.", { userId });
            throw errorHandler.toHttpsError(new functions.https.HttpsError("resource-exhausted", "You are submitting scores too frequently. Please try again later."));
        }

        // Persist the validated score
        // Assuming validatedScore contains all necessary fields including userId, levelId, score, timestamp etc.
        // The document ID could be auto-generated by Firestore or be a composite ID if necessary.
        // For simplicity, let's assume Firestore generates the ID.
        const scoreToSave = {
            ...validatedScore,
            userId: userId,
            // displayName might be fetched or passed, or set by a trigger later
            // timestamp: admin.firestore.FieldValue.serverTimestamp() // This will be handled by firestore.service
        };

        await firestoreService.setDocument(
            config.LEADERBOARD_COLLECTION_NAME,
            // Generating a unique ID for the score entry. Could also be `${userId}_${validatedScore.levelId}` if scores are unique per user/level
            // and we want to overwrite previous scores, or add a timestamp to make it unique.
            // For a general leaderboard, each submission is usually a new entry.
            admin.firestore().collection(config.LEADERBOARD_COLLECTION_NAME).doc().id,
            scoreToSave
        );

        await rateLimiter.recordRequest(userId, actionType);

        logger.info("submitScore: Score submitted successfully.", { userId, scoreId: /* score document ID if available */ validatedScore });
        return { success: true, message: "Score submitted successfully.", validatedScore };

    } catch (error) {
        logger.error("submitScore: Error submitting score.", error, { userId, scoreData });
        if (error instanceof functions.https.HttpsError) {
            throw error;
        }
        throw errorHandler.toHttpsError(new functions.https.HttpsError("internal", "An internal error occurred while submitting the score."));
    }
});

/**
 * Retrieves leaderboard data based on query parameters.
 * @async
 * @function getLeaderboard
 * @param {object} queryParams - Parameters for querying the leaderboard (e.g., levelId, limit, offset, orderBy).
 * @param {functions.https.CallableContext} [context] - The context of the function call (optional, for public leaderboards).
 * @returns {Promise<object>} A promise that resolves with an array of leaderboard entries.
 * @throws {functions.https.HttpsError} Throws an HttpsError for invalid arguments or internal errors.
 */
exports.getLeaderboard = functions.https.onCall(async (queryParams, context) => {
    const userId = context && context.auth ? context.auth.uid : "anonymous";
    logger.info("getLeaderboard function called", { queryParams, userId });

    try {
        const { levelId, limit = 10, orderByField = "score", orderByDirection = "desc" } = queryParams;

        if (!levelId) {
            logger.warn("getLeaderboard: levelId is required.", { queryParams });
            throw errorHandler.toHttpsError(new functions.https.HttpsError("invalid-argument", "levelId is a required query parameter."));
        }

        const filters = [
            { field: "levelId", operator: "==", value: levelId }
        ];
        const orderBy = [{ field: orderByField, direction: orderByDirection }];

        const leaderboardEntries = await firestoreService.queryCollection(
            config.LEADERBOARD_COLLECTION_NAME,
            filters,
            orderBy,
            limit
        );

        logger.info("getLeaderboard: Leaderboard data fetched successfully.", { userId, count: leaderboardEntries.length });
        return { success: true, leaderboard: leaderboardEntries };

    } catch (error) {
        logger.error("getLeaderboard: Error fetching leaderboard.", error, { userId, queryParams });
        if (error instanceof functions.https.HttpsError) {
            throw error;
        }
        throw errorHandler.toHttpsError(new functions.https.HttpsError("internal", "An internal error occurred while fetching the leaderboard."));
    }
});